func newCreateTable() *sqlite3.ReplicationFramesParams {
	params := &sqlite3.ReplicationFramesParams{
		PageSize:  4096,
		Pages:     make([]sqlite3.ReplicationPage, len(createTable)),
		Truncate:  3,
		IsCommit:  1,
		SyncFlags: 10,
	}

	for i, page := range createTable {
		params.Pages[i].Fill(page.Data(), page.Flags, page.Number)
	}

	return params
}

func newInsertN() *sqlite3.ReplicationFramesParams {
	params := &sqlite3.ReplicationFramesParams{
		PageSize:  4096,
		Pages:     make([]sqlite3.ReplicationPage, len(insertN)),
		Truncate:  0,
		IsCommit:  0,
		SyncFlags: 10,
	}

	for i, page := range insertN {
		params.Pages[i].Fill(page.Data(), page.Flags, page.Number)
	}

	return params
}

type pageInfo struct {
	data   string
	Flags  uint16
	Number uint32
}

func (p *pageInfo) Data() []byte {
	data, err := base64.StdEncoding.DecodeString(strings.Map(func(r rune) rune {
		switch r {
		case 0x000A, 0x000B, 0x000C, 0x000D, 0x0085, 0x2028, 0x2029:
			return -1
		default:
			return r
		}
	}, p.data))
	if err != nil {
		panic(err)
	}
	return data
}

